s = 'Welcome to Python'
print('The String: ', s)
print('All characters between index 6 and 10: ',s[6:10])
print('All Characters after the 4th character: ',s[4:])
print('Last 5 characters: ',s[-5:])
print('All characters at Even index: ',s[::2])
print('The String in Reverse: ',s[::-1])
print('Is it a palindrome?  ',s[::-1]==s)
